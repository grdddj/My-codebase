    private void PossibleWinMouseEntered(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_PossibleWinMouseEntered
        try {
            ratio = 100 / (Double.parseDouble(jtxtNumber.getText()));
            bet = Double.parseDouble(jtxtBet.getText());

            double d = bet * (ratio - 1);
            double dd = Math.round(100 * d) / 100;
            String s = String.valueOf(d);

            PossibleWin.setText(String.format("%.2f", d));
        } catch (Exception e) {
            PossibleWin.setText("");
        }    }//GEN-LAST:event_PossibleWinMouseEntered

