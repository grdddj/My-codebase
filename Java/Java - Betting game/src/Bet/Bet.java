    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        File file = new File("D:\\Betting_game\\record.txt");
        file.setWritable(true);
     
               
        
        
        
        boolean hasnotenough = (Double.parseDouble(jtxtBet.getText()) > Double.parseDouble(jtxtPoints.getText()));
        boolean outofbounds = (Integer.parseInt(jtxtNumber.getText()) > 99 || Integer.parseInt(jtxtNumber.getText()) < 0);
        boolean notinteger = (isInteger(jtxtNumber.getText()) == false || isDouble(jtxtBet.getText()) == false);

        if (hasnotenough == true || outofbounds == true || notinteger == true) {
            JOptionPane.showMessageDialog(null, "You did not enter right information", "Error message", JOptionPane.ERROR_MESSAGE);
            jtxtBet.setText(null);
            jtxtNumber.setText(null);
        } else {
            Random rnd = new Random();

            random = rnd.nextInt(100) + 1;
            jtxtResult.setText(String.valueOf(random));
            initial = Double.parseDouble(jtxtPoints.getText());
            ratio = 100 / (Double.parseDouble(jtxtNumber.getText()));
            bet = Double.parseDouble(jtxtBet.getText());
            number = Integer.parseInt(jtxtNumber.getText());

            if (Double.parseDouble(jtxtNumber.getText()) > Double.parseDouble(jtxtResult.getText())) {
                initial += (ratio - 1) * bet;
                initial = (double) Math.round(100 * initial) / 100;
                JOptionPane.showMessageDialog(null, "You win! Your number " + number + " is bigger than " + random + "." + " You have won " + (ratio - 1) * bet + ".", "Winning message", JOptionPane.INFORMATION_MESSAGE);
            } else {
                initial -= bet;
                initial = (double) Math.round(100 * initial) / 100;
                JOptionPane.showMessageDialog(null, "You lost! Your number " + number + " is smaller than " + random + "." + " You have lost " + bet + ".", "Losing message", JOptionPane.INFORMATION_MESSAGE);
            }

            jtxtBet.setText(null);
            jtxtNumber.setText(null);
            jtxtPoints.setText(String.valueOf(initial));

            if (initial == 0) {
                JOptionPane.showMessageDialog(null, "You have lost all your money", "THE END", JOptionPane.INFORMATION_MESSAGE);
                jtxtPoints.setText(String.valueOf(1000));
            }

            // finding out the record
            ArrayList<String> seznam2 = new ArrayList<String>();
            String line;
            double record;

            try (BufferedReader reader = new BufferedReader(new FileReader("D:\\Betting_game\\record.txt"))) {
                line = reader.readLine();
                while (line != null) {
                    seznam2.add(line);
                    line = reader.readLine();

                }
            } catch (Exception e) {
                System.out.println(e.getMessage());
            }

            record = Double.parseDouble(seznam2.get(seznam2.size() - 2));

            if (initial > record) {
                String text = String.valueOf(initial);
                String text2 = jName.getText();
                try (BufferedWriter writer = new BufferedWriter(new FileWriter("D:\\Betting_game\\record.txt", true))) {
                    writer.newLine();
                    writer.write(text);
                    writer.newLine();
                    writer.write(text2);
                    writer.newLine();
                } catch (IOException e) {
                    System.out.println(e.getMessage());
                }

                jtxtRecord.setText(String.valueOf(initial));

                jtxtRecordHolder.setText(jName.getText());
                
                 file.setReadOnly();

            }

        }
    }//GEN-LAST:event_jButton1ActionPerformed

